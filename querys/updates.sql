-- Ejercicios de Actualización Masiva
-- Actualizar Precios de Productos en una Categoría

-- Actualiza el precio de todos los productos en una categoría específica, incrementando su precio en un 10%. Asegúrate de que solo se actualicen los productos de esa categoría.
-- Desactivar Clientes Sin Actividad Reciente

-- Cambia el estado de todos los clientes que no han realizado pedidos en los últimos dos años a "Inactivo".
-- Ajuste de Stock por Ventas Anuladas

-- Si se anula un pedido, restaura el stock de los productos involucrados en ese pedido. Actualiza la cantidad en stock de todos los productos que estaban en el pedido anulado.
-- Aumento de Salario para Empleados con Ventas Elevadas

-- Incrementa en un 5% el salario de todos los empleados que hayan gestionado al menos 50 pedidos en el último año.
-- Reasignación de Proveedor para Productos en Falta

-- Para todos los productos que tienen un stock bajo y cuyo proveedor actual no puede abastecerlos en el próximo mes, reasigna un nuevo proveedor (de la lista de proveedores disponibles) que pueda suministrarlos.
